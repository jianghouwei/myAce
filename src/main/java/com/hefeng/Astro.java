public class Astro {

	/* 月升时间 */
	private String mr;
	/* 月落时间 */
	private String ms;
	/* 日出时间 */
	private String sr;
	/* 日落时间 */
	private String ss;

	public void setMr(String mr) {
		this.mr = mr;
	}

	public String getMr() {
		return mr;
	}

	public void setMs(String ms) {
		this.ms = ms;
	}

	public String getMs() {
		return ms;
	}

	public void setSr(String sr) {
		this.sr = sr;
	}

	public String getSr() {
		return sr;
	}

	public void setSs(String ss) {
		this.ss = ss;
	}

	public String getSs() {
		return ss;
	}

}